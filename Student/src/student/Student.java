package student;

public class Student {
	String Age;
	String No;
	String Address;
	String Num;
		
	public void printAge() {
		System.out.println("나이 출력 : " + Age);
	}

	public void printNo() {
		System.out.println("학번 출력 : " + No);
	}
	
	public void printAddress() {
		System.out.println("주소 출력 : " + Address);
	}
		
	public void printNum() {
		System.out.println("전화번호 출력 : " + Num);
	}
}
